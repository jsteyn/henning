<h1>HENNINGWAPPEN </h1>
<p>
<a href="eng-henningwapens.php" title="Henning">English</a>&nbsp;|&nbsp;<a href="afr-henningwapens.php" title="Henningwapens">Afrikaans</a>
</p>
<p>W&auml;hrend des Mittelalters wurden Familienwappen im Adelstand und im Ritterorden eingef&uuml;hrt. Ein Wappen wurde auf dem Schild, mit dem die Person sich verteidigte,oder an milit&auml;rischen Turnieren teilnahm, gemalt. Dieses Wappen diente also zur pers&ouml;nlichen Identifizierung und wurde auch als Siegel gebraucht, fast wie eine Unterschrift heute. 
</p>

<p>Nach einiger Zeit &uuml;bernahm der &auml;lteste Sohn aber das Schild seines Vaters (und das Wappen auf dem Schild). Noch sp&auml;ter wurden diese Wappen als Familienwappen &uuml;bernommen.</p>

<p>Bis jetzt wissen wir von nur drei Henningfamilien - eine in S&uuml;dafrika und zwei in Deutschland, die spezifische Familienwappen haben. Veschiedene Henning/Hennings/Hennigwappen entstanden durch die Jahrhunderte. In der einflussreichen Ver&ouml;ffentlichung "Armorial G&eacute;n&eacute;ral" von J.B. Rietstap, welche durch Rolland illustriert ist, erscheinen nicht weniger als 112 000 Familienwappen, wovon elf(11) als Henning/Hennings/Hennigwappen identifiziert sind. Diese Wappen werden auch abgebildet.
</p>

