<p>Die naam Henning is inheems tot die Noord Duitse gebiede Mecklenburg, Hannover, Hamberg, Holstein en Pommern. Veral die dorpe Stralsund en Greiffswald, in Mecklenburg, naby die Baltiese See staan bekend as plekke waar die naam ontstaan het. Beide hierdie dorpe was tot en met die Dertigjarige-Oorlog (1618 &mdash; 1648) deel van Denemarke. Tussen die jare 1300 en 1500 is die naam HENNING as 'n gewilde bynaam aan Johannes se seun gegee. In die ou Baltiese dialek is 'n hoenderhaan 'n "hen" genoem (hen = manlik, henne = vroulik). Die Baltiese (of Noord Duitse) "ing" is bygevoeg om aan te dui dat dit 'n naam is wat afgelei is van 'n vader of voorsaat se naam. In moderne Afrikaans sou ons hom Haantjie genoem het.  Dit was dus 'n vriendelike manier om so 'n persoon aan te spreek.</p>
<p>Die naam het ontstaan onder edelmanne en ridders, soos<sup>1</sup>:</p>
<ul>
<li>Henning = Johannes Older, wat in 1290 in die omgewing van Stralsund gewoon het.</li>
<li>Henning = Johannes Brunswick, wat in 1305 te Kolberg gewoon het. Kolberg val vandag binne die grondgebied van Pole, maar was gedurende 1305 deel van die Staat wat vandag Duitsland genoem word.</li>
<li>Henning = Johannes Dotenberg (Ridder), wat in 1326 in die omgewing van Greiffswald woonagtig was. Daar word vermoed dat hy 'n nasaat van Johannes Older was. </li>
<li>Graaf Henning van Irkesleve wat in 1330 in Haldslohe gewoon het. </li>
</ul>
<p>Gedurende die 13/14e eeue het nasate van hierdie eerste Hennings versprei oor die gebiede wat vandag as Duitsland, Denemarke en Noorwe&euml; bekend is. Alle rekords uit hierdie tyd het egter met verloop van tyd verlore gegaan.
