<p>
Gedurende die 17e en 18e eeu het verskeie Hennings begin om elders heen te verhuis &mdash; ook na die nuwe w&ecirc;reld. So vind ons vandag een van die grootste Henning stamme in Suid-Afrika. Henning gesinne word vandag egter in bykans al die beskaafde lande van die Westerse w&ecirc;reld gevind. 
</p><p>
'n Paar Henning nasate het egter in die omgewing van Stralsund en Greiffswald agtergebly. Dit is bekend dat Jacob Henning (die oue), gedurende 1635 te Demmin (naby Greiffswald) gebore is. Jacob Henning en sy nasate het  as die Henning familie van Karnin bekend geword. Karnin is ongeveer 25 km vanaf Stralsund. Jacob Henning en sy nasate was groot en welgestelde grondeienaars wat in 'n kasteel gewoon het. Hierdie kasteel is gedurende 1945, tydens die Tweede W&ecirc;reldoorlog deur die Kommunistiese regering, wat Oos-Duitsland oorgeneem het, gekonfiskeer. Die familie het hierna oor Wes-Duitsland versprei.</p>
