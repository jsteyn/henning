<p>During the 17th and 18th Centuries several Hennings moved elsewhere &mdash; also to the new world. Today we find one of the biggest Henning clans in South Africa. However, we find Henning families in almost every civilised country in the Western world.
</p><p>
A few Henning descendants, however, remained in the vicinity of Stralsund and Greiffswald. It is known that Jacob Henning (the old) was born during 1635 at Demmin (near Greiffswald). Jacob Henning and his descendants became  known as the Henning Family of Karnin. Karnin is approximately 25 km from Stralsund. Jacob Henning and his descendants were big and wealthy property owners, who lived in a castle. This castle was confiscated during 1945, during the Second World War, by the communist government who took over Eastern Germany. Consequently this family spread over Western Germany.</p>
