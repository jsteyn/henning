<p>The name Henning is indigenous to the North German areas Mecklenburg, Hannover, Hamberg, Holstein and Pommern. Especially the towns Stralsund and Greiffswald, in Mecklenburg, near the Baltic Sea is well known as places where the name originated. Both towns formed part of Denmark up until the Thirty Years War (1618 &mdash; 1648).</p>
<p>Between the years 1300 and 1500 the name HENNING was used as a popular nickname for "the Son of John (Johannes)". In the old Baltic dialect a rooster was called a "Hen" (hen = male, henne = female). The Baltic (or North German) "ing" was added to indicate that it was a name deduced from the name of a father or ancestor.</p>
<p>The name originated amongst noblemen and knights, such as<sup>1</sup>:</p>
<ul>
<li>Henning = Johannes Older, who lived during 1290 in the vicinity of Stralsund;</li>
<li>Henning = Johannes Brunswick, who lived during 1305 at Kolberg. Today Kolberg is in Poland, but during 1305 it formed part of the country which is known today as Germany.</li>
<li>Henning = Johannes Dotenberg (Knight) who lived during 1326 in the vicinity of Greiffswald. It is suspected that he was a descendant of Johannes Older.</li>
<li>Earl Henning of Irkesleve who lived during 1330 in Haldslohe.</li>
</ul>
<p>
During the 13/14th centuries the descendants of these first Hennings spread over the areas today known as Germany, Denmark and Norway. All records from this time unfortunately were destroyed.</p>
